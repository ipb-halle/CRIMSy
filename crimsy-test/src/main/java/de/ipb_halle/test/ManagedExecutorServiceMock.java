package de.ipb_halle.test;

import java.util.ArrayList;
import java.util.Collection;
import java.util.List;
import java.util.concurrent.Callable;
import java.util.concurrent.ExecutionException;
import java.util.concurrent.Future;
import java.util.concurrent.RejectedExecutionException;
import java.util.concurrent.TimeUnit;
import java.util.concurrent.TimeoutException;

import javax.enterprise.concurrent.ManagedExecutorService;

/**
 * Mock implementation of ManagedExecutorService that can hold maxTasks submitted
 * tasks.
 * 
 * @author flange
 */
public class ManagedExecutorServiceMock implements ManagedExecutorService {

    private int maxTasks;
    private List<Runnable> submittedTasks = new ArrayList<>();

    public ManagedExecutorServiceMock(int maxTasks) {
        this.maxTasks = maxTasks;
    }

    @Override
    public Future<?> submit(Runnable task) {
        if (submittedTasks.size() < maxTasks) {
            submittedTasks.add(task);
        } else {
            throw new RejectedExecutionException();
        }
        return null;
    }

    public void reset() {
        submittedTasks.clear();
    }

    public List<Runnable> getSubmittedTasks() {
        return submittedTasks;
    }

    @Override
    public void shutdown() {
    }

    @Override
    public List<Runnable> shutdownNow() {
        return null;
    }

    @Override
    public boolean isShutdown() {
        return false;
    }

    @Override
    public boolean isTerminated() {
        return false;
    }

    @Override
    public boolean awaitTermination(long timeout, TimeUnit unit) throws InterruptedException {
        return false;
    }

    @Override
    public <T> Future<T> submit(Callable<T> task) {
        return null;
    }

    @Override
    public <T> Future<T> submit(Runnable task, T result) {
        return null;
    }

    @Override
    public <T> List<Future<T>> invokeAll(Collection<? extends Callable<T>> tasks) throws InterruptedException {
        return null;
    }

    @Override
    public <T> List<Future<T>> invokeAll(Collection<? extends Callable<T>> tasks, long timeout, TimeUnit unit)
            throws InterruptedException {
        return null;
    }

    @Override
    public <T> T invokeAny(Collection<? extends Callable<T>> tasks) throws InterruptedException, ExecutionException {
        return null;
    }

    @Override
    public <T> T invokeAny(Collection<? extends Callable<T>> tasks, long timeout, TimeUnit unit)
            throws InterruptedException, ExecutionException, TimeoutException {
        return null;
    }

    @Override
    public void execute(Runnable command) {
    }
}
